
public class Text
{
	private String text;
	private int key;
	
	public Text()
	{
		this.text=text;
		this.key=key;
	}

	public String getText()
	{
		return text;
	}
	public int getKey()
	{
		return key;
	}
	
	public void setText(String text)
	{
		this.text=text;
	}
	public void  setKey(int key)
	{
		this.key=key;
	}	
}